    public function index()
    {
        $users = User::select('id', 'name', 'created_at')
        ->when(Request::get('search'), function($query, $search) {
            $query->where('name', 'like', '%'. $search . '%');
        })
        ->paginate();
        
        return Inertia::render('Users/Index', [
            'users' => $users
        ]);
    }

